var userMonth=prompt('enter a month');
switch(userMonth){
    case 'Monday':
        alert('Երկուշաբթի');
        break;
     case 'Tuesday':
        alert('Երեքշաբթի');
        break;   
      case 'Wednesday':
        alert('Չորեքշաբթի');
        break;   
      case 'Thursday':
        alert('Հինգշաբթի');
        break;
      case 'Friday':
        alert('Ուրբաթ');
        break;
      case 'Saturday':
        alert('Շաբաթ');
        break;
      case 'Sunday':
        alert('Կիրակի');
        break;
    default:
        alert('invalid month')
      
}